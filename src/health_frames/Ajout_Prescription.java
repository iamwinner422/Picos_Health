    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        this.boxPosologie.setBackground(new Color(14,22,33));
        //SELECTION DES INFOS DE LA CONSULTATION
        String[] colonne = {"numConsult", "CONCAT_WS('     ', dateConsult, nomMedecin) AS date_et_medecin"};
        rs = db.requeteSelectP(colonne, "medecin, consultation", "medecin.numMedecin = consultation.numMedecin");
        
        try {
            while (rs.next()) {
                String numConsult = rs.getString("numConsult").toString();
                String dateConsult = rs.getString("date_et_medecin").toString();
                comConsult.addItem(numConsult+" " + dateConsult);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Ajout_Prescription.class.getName()).log(Level.SEVERE, null, ex);
        }
        //SELECTION DES MEDOCS
        String[] colonnes = {"codeMedoc", "nomMedoc"};
        rs1 = db.requeteSelect(colonnes, "medicament");
        try {
            while (rs1.next()) {
                String nomMedoc = rs1.getString("nomMedoc");
                String numMedoc = rs1.getString("codeMedoc");
                comMedoc.addItem(numMedoc+" "+nomMedoc);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Ajout_Prescription.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formWindowOpened

